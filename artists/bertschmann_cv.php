<td width="900" align="center"><br /><p class="paragraph">It's hard to find many abstract painters today who can match Harry Bertschmann's extraordinary dynamism and versatility. He was born in Basel, Switzerland, where his approach to painting was established at the famous Basel School of Design in the late 1940s. He has been a New Yorker since the 1960s. It is intriguing that ever since he came to America he has led double lives. By day he has been an innovator in conceptual graphic design — and many of his commercial logos and packaging have become iconic in American culture. By night he was a painter driven to push the boundaries of abstract expressionist painting. Ultimately, his works have great staying power. There are no one-liners here, so easy to grasp, as are Andy Warhol’s assembly line screenprints. Rather, a Bertschmann painting compels the viewer to linger — and upon returning find invigoration anew.</p></td>
