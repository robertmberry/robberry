<td width="900" align="center"><br /><p class="paragraph">Geoffrey Moss has led double lives since 1961.<br />
<br />
The public knows him for his instantly recognizable style of searing political cartoons that appeared in the op-ed pages of the Washington Post for decades. He was nominated two times for the Pulitzer Prize and became one of the most sought-after illustrators in the country. But during all that time — ever since 1961, when he entered Yale University’s Master of Fine Arts program — he has privately led a double life as an abstract painter.<br />
<br />
Moss is as methodical in his approach to abstraction as he has been in his political satire. Every canvas starts with pencil drawings; he then moves on to a series of increasingly larger color studies until he is ready to approach the large canvas. “I define myself as an American painter, paying attention to the essence of an idea. Painting in series, exploring the physical energy of the paint, isolating my subject. I continually return to re-examine, simplify, and abstract the essentials in my work.” In September 2013 he revealed his first major gallery exhibition — “Inappropriate Appropriations” — at Studio Vendome in SoHo. This compelling body of work represents his most private and unseen paintings. This series uses as its reference point the classic erotica imagery of Japanese shunga painting found in ukiyo-e color woodblock prints of the 17th and 18th centuries, a subject with which he first became familiar while working at the Metropolitan Museum of Art. “I wanted to give this Japanese subject an American character…It’s a process of distillation in many steps” until the erotic image becomes abstracted yet still bearing a subliminal seduction.</p></td>
